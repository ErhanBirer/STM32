/**
  * @brief  Timer Kesme Callback Fonksiyonu
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
    // Kontrol periyodu (TIM2 - 100Hz/10ms)
    if(htim->Instance == TIM2)
    {

    	HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14, GPIO_PIN_SET);
        // 1. Enkoder Farkını Oku (Hız Tespiti)
        // int16_t kullanımı 0-65535 geçişlerini otomatik düzeltir
        int16_t encoder_value = (int16_t)__HAL_TIM_GET_COUNTER(&htim1);
        __HAL_TIM_SET_COUNTER(&htim1, 0); // Bir sonraki fark için sıfırla

        // Pulse değerini RPM'e çevir (Opsiyonel)
        // actual = ((float)encoder_value / TICKS_PER_REV) * (60.0f / dt);
        actual = ((float)encoder_value / 4096.0f) * 6000.0f;

        // 2. Hata (Error) Hesaplama
        float onceki_error = error;
        error = referance - actual;

        // 3. Türev Hesaplama (Filtreli / de/dt)
        float error_turev_ham = (error - onceki_error) / dt;
        turev_filtreli = (alpha * error_turev_ham) + (1.0f - alpha) * turev_filtreli;
        turev = turev_filtreli;

        // 4. İntegral Hesaplama (Anti-Windup ile)
        integral_sonuc += error * dt;

        // İntegral Sınırlandırma (Sisteminize göre limitleri güncelleyin)
        if(integral_sonuc > 500.0f) integral_sonuc = 500.0f;
        if(integral_sonuc < -500.0f) integral_sonuc = -500.0f;

        // 5. Bir sonraki adım için sakla
        speed_onceki = actual;
        pwm = (10*error) +(2*turev)+(0.1*integral_sonuc);

    }
}
